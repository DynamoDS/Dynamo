using Dynamo.UI.Commands;

namespace Dynamo.Search.SearchElements
{
    public partial class NodeSearchElement
    {
        private DelegateCommand _toggleDescriptionVisibilityCommand;

        /// <summary>
        /// Command for toggling visibility
        /// </summary>
        public DelegateCommand ToggleDescriptionVisibilityCommand
        {
            get
            {
                if(_toggleDescriptionVisibilityCommand == null)
                    _toggleDescriptionVisibilityCommand = new DelegateCommand(ToggleIsVisible);
                return _toggleDescriptionVisibilityCommand;
            }
        }
    }
}
